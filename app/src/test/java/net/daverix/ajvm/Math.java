package net.daverix.ajvm;

public class Math {
    public static int add(int first, int second) {
        return first + second;
    }

    public static int subtract(int first, int second) {
        return first - second;
    }

    public static int multiply(int first, int second) {
        return first * second;
    }

    public static int divide(int first, int second) {
        return first / second;
    }

    public static int mod(int first, int second) {
        return first % second;
    }

    public static float add(float first, float second) {
        return first + second;
    }

    public static float subtract(float first, float second) {
        return first - second;
    }

    public static float multiply(float first, float second) {
        return first * second;
    }

    public static float divide(float first, float second) {
        return first / second;
    }

    public static double add(double first, double second) {
        return first + second;
    }

    public static double subtract(double first, double second) {
        return first - second;
    }

    public static double multiply(double first, double second) {
        return first * second;
    }

    public static double divide(double first, double second) {
        return first / second;
    }

    public static long add(long first, long second) {
        return first + second;
    }

    public static long subtract(long first, long second) {
        return first - second;
    }

    public static long multiply(long first, long second) {
        return first * second;
    }

    public static long divide(long first, long second) {
        return first / second;
    }

    public static long mod(long first, long second) {
        return first / second;
    }

    public static short add(short first, short second) {
        return (short) (first + second);
    }
}
